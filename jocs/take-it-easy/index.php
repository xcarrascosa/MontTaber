                        que sea igual a la que ha seleccionado el locutor. Cada jugador, incluido el locutor, coloca esa
                        ficha en su tablero personal. Una vez que todos los jugadores hayan colocado su ficha en su
                        tablero, el locutor selecciona la siguiente ficha. El juego continúa así hasta que los tableros
                        estén completos.</p>
                    <p>Las fichas siempre deben estar colocadas de tal manera que los números se puedan leer de frente
